b1 = 17
b2 = 6

# operasi tambah "+"
tambah = b1 + b2
print (tambah)

# Operasi pengurangan "-"
pengurangan = b1 - b2
print (pengurangan)

# Operasi perkalian "*"
perkalian = b1 * b2
print (perkalian)

# Operasi pembagian "/"
bagi = b1 / b2
print (bagi)

# Operasi pangkat "**"
pangkat = b1 ** b2
print (pangkat)

# Operasi modulus "%"
modulus = b1
print (modulus)
